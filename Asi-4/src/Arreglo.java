public class Arreglo {
    public static void main(String[] args) {
        int[] numeros = {5, 8, 12, 3, 9, 7, 4, 11, 2, 10};

        // Mostrar el primer y el último valor del arreglo
        System.out.println("Primer valor: " + numeros[0]);
        System.out.println("Último valor: " + numeros[numeros.length - 1]);
    }
}
